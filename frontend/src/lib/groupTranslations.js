// Hook: useGroupT - returns gt() function for group pages
export function useGroupT() {
  const { lang, switchLanguage, t, isReady, languages } = useLanguage();

  const gt = (key, replacements = {}) => {
    // First check group-specific translations
    let text = groupStrings[lang]?.[key] || groupStrings['en']?.[key] || t(key);
    // Handle replacements like {name}, {shown}, {total}
    Object.keys(replacements).forEach((k) => {
      text = text.replace(`{${k}}`, replacements[k]);
    });
    return text;
  };

  return { lang, switchLanguage, gt, t, isReady, languages };
}

